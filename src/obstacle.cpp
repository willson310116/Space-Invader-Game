Obstacle::Obstacle(Vector2 position)
    : position(position)
{   
    for (int i = 0; i < grid.size(); i++)
    {
        for (int j = 0; j < grid[0].size(); j++)
        {
            if (grid[i][j] == 1)
            {
                float pos_x = position.x + j * 3;
                float pos_y = position.y + i * 3;
                blocks.emplace_back(Vector2{pos_x, pos_y});
            }
        }
    }
}

void Obstacle::Draw()
{
    for (auto& block : blocks)
        block.Draw();
}